// problem: Write a program that uses the ternary operator to check if a number is positive or negative and log the result to the console.

const number1 = 45;
const number2 = -45;
const result1 = number1>=0 ?'number is positive' :'number is negative';
const result2 = number2>=0 ?'number is positive': 'number is negative';
