// problem: Create an array of numbers from 1 to 5 and log the array to the console. Access the first and last elements of the array and log them to the console.
const arr = [1,2,3,4,5];
console.log(arr); // [1,2,3,4,5]
console.log(`The first element is: ${arr[0]}`); // The first element is: 1
console.log(`The last element is: ${arr[arr.length -1]}`); // The last element is: 5